def data(name):
    with open(name) as f:
      data = json.load(f)
      for item in data:
          yield f"<s>[INST] {item['text']} [/INST]"


def result(msg):
    sub1 = "[/INST]"
    sub2 = "</s>"

    test_str=msg[0]["generated_text"].replace(sub1,"*")
    test_str=test_str.replace(sub2,"*")
    re=test_str.split("*")
    res=re[1]
    filter_before = res.split(".")
    filter_after = list(dict.fromkeys(filter_before))
    if len(filter_before) != len(filter_after):
        filter_after.pop()
    res = ".".join(filter_after)
    return res

def chatbot(message):
    input_str = "<s>[INST]" + message + "[/INST]"
    return result(pipe(input_str))